    private void kullaniciGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kullaniciGuncelleActionPerformed
        
        String username = ad_alani.getText();
        String password = parola_alani.getText();
        String telefon = telefon_alani.getText();
        String email = email_alani.getText();
        
        int selectedrow = kullanici_tablosu.getSelectedRow();
        
        if (selectedrow == -1) {
            
            if(model.getRowCount() == 0) {
                
                mesaj_yazisi.setText("Kullanıcı Tablosu şu anda boş");     
            } else {
                
                 mesaj_yazisi.setText("Güncellemek istediğiniz üyeyi seçmediğiniz için işlem yapılamamaktadır"); 
            }  
        } else {
            
            int user_id = (int) model.getValueAt(selectedrow, 0);
            
            islemler.uyeGuncelle(user_id,username,password,telefon,email);
            uyeleriGoruntule();
            mesaj_yazisi.setText("Üye başarıyla güncellendi.");
        }
    }//GEN-LAST:event_kullaniciGuncelleActionPerformed

    private void kullanici_tablosuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kullanici_tablosuMouseClicked
        
        int selectedrow = kullanici_tablosu.getSelectedRow();
        ad_alani .setText(model.getValueAt(selectedrow,1).toString());
        parola_alani .setText(model.getValueAt(selectedrow,2).toString());
        telefon_alani .setText(model.getValueAt(selectedrow,4).toString());
        email_alani .setText(model.getValueAt(selectedrow,5).toString());
    }//GEN-LAST:event_kullanici_tablosuMouseClicked

    private void kullaniciSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kullaniciSilActionPerformed
         mesaj_yazisi.setText("");
        
        int selectedrow = kullanici_tablosu.getSelectedRow();
        
        if (selectedrow == -1){
            
            if (model.getRowCount() == 0){
                
                mesaj_yazisi.setText("Kullanıcı Tablosu şu anda boş");   
            } else {
                mesaj_yazisi.setText("Lütfen silmek istediğiniz kitabı seçiniz..."); 
            } 
        } else {
            
            int book_id = (int) model.getValueAt(selectedrow,0);
            
            islemler.uyeSil(book_id);
            uyeleriGoruntule();
            mesaj_yazisi.setText("Üye başarıyla silindi.");
            
        } 
    }//GEN-LAST:event_kullaniciSilActionPerformed

