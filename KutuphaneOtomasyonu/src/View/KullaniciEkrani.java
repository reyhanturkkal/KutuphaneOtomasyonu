     // Kitap tablosunun Görüntülenmesi
    public void uyeleriGoruntule(){
        
        model.setRowCount(0);
        ArrayList<Uyeler> uye = new ArrayList<Uyeler>();
        
        
        EkranIslemContext uyeleriGetir = new EkranIslemContext(islemler);
        uye = uyeleriGetir.calistirStrategy();
        
        if(uye != null) {
            
            for(Uyeler uyeler : uye) {
                Object[] eklenecek = {uyeler.getUser_id(),uyeler.getUsername(),uyeler.getPassword(),uyeler.getUserType(),uyeler.getTelefon(),uyeler.getEmail()};
                model.addRow(eklenecek);
            }
            
        }
        
        
    }
