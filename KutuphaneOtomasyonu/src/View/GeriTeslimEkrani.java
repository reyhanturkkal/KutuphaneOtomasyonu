     // Odunc tablosunun Görüntülenmesi
    public void oduncGoruntule(){
        
        model.setRowCount(0);
        ArrayList<Odunc> oduncler = new ArrayList<Odunc>();
        
        EkranIslemContext oduncleriGetir = new EkranIslemContext(teslimislemler);
        oduncler = oduncleriGetir.calistirStrategy();
        
        
        if(oduncler != null) {
            
            for(Odunc odunc : oduncler) {
                Object[] eklenecek = {odunc.getBook_id(),odunc.getUser_id(),odunc.getLoan_date(),odunc.getReturn_date()};
                model.addRow(eklenecek);
            }
            
        }
        
        
    }
