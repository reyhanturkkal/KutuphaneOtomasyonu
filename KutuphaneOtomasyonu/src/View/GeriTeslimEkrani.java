    private void teslimEtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_teslimEtActionPerformed
       mesaj_yazisi.setText("");

    int selectedrow = odunc_tablosu.getSelectedRow();

    if (selectedrow == -1) {
        if (model.getRowCount() == 0) {
            mesaj_yazisi.setText("Kitap Tablosu şu anda boş");
        } else {
            mesaj_yazisi.setText("Lütfen silmek istediğiniz kitabı seçiniz...");
        }
    } else {
        int book_id = (int) model.getValueAt(selectedrow, 0);

        teslimislemler.oduncTeslimEt(book_id);
        oduncGoruntule();
        mesaj_yazisi.setText("Teslim işlemi başarıyla gerçekleştirildi.");
    }
    }//GEN-LAST:event_teslimEtActionPerformed

